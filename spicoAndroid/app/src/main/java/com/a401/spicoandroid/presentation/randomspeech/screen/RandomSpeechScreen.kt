@Composable
fun RandomSpeechScreen(
    viewModel: RandomSpeechSharedViewModel,
    navController: NavController = LocalNavController.current,
    onFinish: () -> Unit = {}
) {
    val uiState by viewModel.uiState.collectAsState()
    val question = uiState.question
    val speakMin = uiState.speakTime / 60

    val coroutineScope = rememberCoroutineScope()
    val context = LocalContext.current
    val activity = context as? Activity

    var showExitAlert by remember { mutableStateOf(false) }
    var showExitConfirmDialog by remember { mutableStateOf(false) }
    var showForceExitAlert by remember { mutableStateOf(false) }

    var prepCountdown by remember { mutableIntStateOf(3) }
    var startMainTimer by remember { mutableStateOf(false) }

    val waveform = remember { mutableStateOf(emptyList<Float>()) }
    val audioAnalyzer = remember { AudioAnalyzer() }
    val audioRecorderService = remember { AudioRecorderService(context) }

    val recordedFile = remember { mutableStateOf<File?>(null) }

    // Í∂åÌïú ÏöîÏ≤≠
    LaunchedEffect(Unit) {
        if (ContextCompat.checkSelfPermission(context, Manifest.permission.RECORD_AUDIO)
            != PackageManager.PERMISSION_GRANTED
        ) {
            ActivityCompat.requestPermissions(
                activity ?: return@LaunchedEffect,
                arrayOf(Manifest.permission.RECORD_AUDIO),
                1001
            )
        }
    }

    // Ï§ÄÎπÑ ÌÉÄÏù¥Î®∏
    LaunchedEffect(Unit) {
        while (prepCountdown > 0) {
            delay(1000)
            prepCountdown--
        }
        startMainTimer = true
    }

    // ÎÖπÏùå ÏãúÏûë
    LaunchedEffect(startMainTimer) {
        if (startMainTimer) {
            audioRecorderService.start(
                scope = coroutineScope,
                onWaveformUpdate = { amps ->
                    waveform.value = amps
                },
                onComplete = { file ->
                    Log.d("ExitFlow", "‚úÖ onComplete ÏßÑÏûÖ, ÌååÏùº: ${file.name}")
                    recordedFile.value = file
                    coroutineScope.launch {
                        try {
                            viewModel.setLoading(true)
                            val transcript = WhisperApiHelper.transcribeWavFile(file)
                            Log.d("ExitFlow", "üìù Whisper Î≥ÄÌôò ÏôÑÎ£å")

                            viewModel.submitScript(
                                script = transcript,
                                onSuccess = {
                                    val id = viewModel.getSpeechIdForReport()
                                    if (id != null) {
                                        coroutineScope.launch {
                                            delay(300)
                                            navController.navigate(
                                                route = NavRoutes.RandomSpeechReport.withId(id),
                                                navOptions = navOptions {
                                                    popUpTo(NavRoutes.RandomSpeech.route) { inclusive = true }
                                                }
                                            )
                                        }
                                    } else {
                                        Log.e("ExitFlow", "‚ùå idÍ∞Ä nullÏù¥Îùº Ïù¥ÎèôÌï† Ïàò ÏóÜÏäµÎãàÎã§.")
                                    }
                                },
                                onError = {
                                    Log.e("ExitFlow", "‚ùå Î¶¨Ìè¨Ìä∏ Ï†ÄÏû• Ïã§Ìå®")
                                }
                            )
                        } catch (e: Exception) {
                            Log.e("ExitFlow", "‚ùå STT Ïã§Ìå®: ${e.message}")
                        }
                    }
                }
            )
        }
    }

    // Ï†úÌïú ÏãúÍ∞ÑÏù¥ ÏßÄÎÇòÎ©¥ Ï¢ÖÎ£å ÏöîÏ≤≠
    val totalSeconds = speakMin * 60
    val remainingSeconds by countdownTimer(
        totalSeconds = totalSeconds,
        isRunning = startMainTimer,
        onFinish = {
            Log.d("ExitFlow", "‚úÖ Ïπ¥Ïö¥Ìä∏Îã§Ïö¥ ÎÅù! ÎÖπÏùå Ï†ïÏßÄ Ìò∏Ï∂ú")
            audioRecorderService.stop()
            val file = recordedFile.value
            if (file != null) {
            }
        }
    )

    val elapsedSeconds by rememberElapsedSeconds(isRunning = startMainTimer)

    fun handleExit() {
        if (elapsedSeconds < 30) {
            showExitConfirmDialog = true
        } else {
            showExitAlert = true
        }
    }

    // Îí§Î°ú Í∞ÄÍ∏∞
    BackHandler {
        if (uiState.isLoading) {
            Toast.makeText(context, "Ï†ÄÏû• Ï§ëÏûÖÎãàÎã§!", Toast.LENGTH_SHORT).show()
        } else if (prepCountdown <= 0) {
            handleExit()
        }
    }

    Box(modifier = Modifier.fillMaxSize()) {
        Scaffold(
            topBar = {
                CommonTopBar(
                    centerText = "ÎûúÎç§Ïä§ÌîºÏπò",
                    rightContent = {
                        CommonButton(
                            text = "Ï¢ÖÎ£å",
                            size = ButtonSize.XS,
                            backgroundColor = Error,
                            borderColor = Error,
                            textColor = White,
                            disabledBackgroundColor = Error,
                            disabledBorderColor = Error,
                            disabledTextColor = White,
                            enabled = prepCountdown == 0,
                            onClick = { handleExit() },
                        )
                    }
                )
            }
        ) { innerPadding ->
            Column(
                modifier = Modifier
                    .fillMaxSize()
                    .background(BrokenWhite)
                    .padding(innerPadding)
                    .padding(horizontal = 20.dp),
                verticalArrangement = Arrangement.SpaceBetween,
                horizontalAlignment = Alignment.CenterHorizontally
            ) {
                Spacer(modifier = Modifier.height(32.dp))

                Box(
                    modifier = Modifier
                        .size(72.dp)
                        .background(White, shape = RoundedCornerShape(32.dp)),
                    contentAlignment = Alignment.Center
                ) {
                    Image(
                        painter = painterResource(id = R.drawable.img_feedback_volume),
                        contentDescription = "ÏùåÏÑ± ÌîºÎìúÎ∞±",
                        modifier = Modifier.size(48.dp)
                    )
                }

                Spacer(modifier = Modifier.height(16.dp))

                InfoSection(title = null) {
                    Text(
                        text = question,
                        style = Typography.bodyLarge,
                        color = TextPrimary
                    )
                }

                Spacer(modifier = Modifier.height(32.dp))

                // Ïã§ÏãúÍ∞Ñ ÏùåÏÑ± ÌååÌòï
                RandomSpeechWaveformView(
                    waveform = waveform.value,
                    modifier = Modifier
                        .fillMaxWidth()
                        .height(120.dp)
                )

                Spacer(modifier = Modifier.height(24.dp))

                Text(
                    text = String.format(
                        Locale.US,
                        "%02d:%02d",
                        remainingSeconds / 60,
                        remainingSeconds % 60
                    ),
                    style = TextStyle(
                        fontFamily = Pretendard,
                        fontWeight = FontWeight.SemiBold,
                        fontSize = 40.sp
                    ),
                    color = TextPrimary
                )

                Spacer(modifier = Modifier.height(24.dp))

                LargeIconCircleButton(
                    icon = {
                        Icon(
                            painter = painterResource(id = R.drawable.ic_microphone_white),
                            contentDescription = "ÎßàÏù¥ÌÅ¨",
                            tint = White,
                            modifier = Modifier.size(32.dp)
                        )
                    },
                    size = 86.dp,
                    borderWidth = 6.dp,
                    borderColor = Disabled,
                    backgroundColor = Action,
                    enabled = false,
                    onClick = { }
                )

                Spacer(modifier = Modifier.height(48.dp))
            }

            fun handleConfirmedExit() {
                val file = recordedFile.value ?: return

                coroutineScope.launch {
                    viewModel.setLoading(true)
                    try {
                        val transcript = WhisperApiHelper.transcribeWavFile(file)
                        viewModel.submitScript(
                            script = transcript,
                            onSuccess = {
                                val id = viewModel.getSpeechIdForReport()
                                if (viewModel.shouldRedirectToReport && id != null) {
                                    navController.navigate(NavRoutes.RandomSpeechReport.withId(id))
                                }
                            },
                            onError = {
                                Toast.makeText(context, "Ïä§ÌÅ¨Î¶ΩÌä∏ Ï†ÄÏû• Ïã§Ìå®", Toast.LENGTH_SHORT).show()
                            }
                        )
                    } catch (e: Exception) {
                        Toast.makeText(context, "ÏùåÏÑ± Ïù∏Ïãù Ïã§Ìå®", Toast.LENGTH_SHORT).show()
                    }
                }
            }

            // 30Ï¥à Ïù¥ÏÉÅ Ï¢ÖÎ£å Îã§Ïù¥ÏñºÎ°úÍ∑∏
            if (showExitAlert) {
                RandomSpeechExitAlert(
                    onDismissRequest = { showExitAlert = false },
                    onCancel = { showExitAlert = false },
                    onConfirm = {
                        showExitAlert = false
                        viewModel.setLoading(true)
                        audioRecorderService.stop()
                        handleConfirmedExit()

                        Log.d("ExitFlow", "üé§ audioRecorderService stop")

                        val file = recordedFile.value
                        if (file != null) {
                            Log.d("ExitFlow", "üìÅ ÎÖπÏùå ÌååÏùº Ï°¥Ïû¨: ${file.name}")
                            coroutineScope.launch {
                                try {
                                    val transcript = WhisperApiHelper.transcribeWavFile(file)
                                    Log.d("ExitFlow", "üó£Ô∏è Whisper Î≥ÄÌôò ÏôÑÎ£å: $transcript")

                                    viewModel.submitScript(
                                        script = transcript,
                                        onSuccess = {
                                            val id = viewModel.getSpeechIdForReport()
                                            Log.d("ExitFlow", "‚úÖ Î¶¨Ìè¨Ìä∏ Ï†ÄÏû• ÏÑ±Í≥µ, id = $id, shouldRedirect = ${viewModel.shouldRedirectToReport}")

                                            // üéØ Ï°∞Í±¥Ïóê Îî∞Îùº ÎÑ§ÎπÑÍ≤åÏù¥ÏÖò Í≤∞Ï†ï
                                            if (viewModel.shouldRedirectToReport && id != null) {
                                                navController.navigate(NavRoutes.RandomSpeechReport.withId(id))
                                            }
                                        },
                                        onError = {
                                            Log.e("ExitFlow", "‚ùå Î¶¨Ìè¨Ìä∏ Ï†ÄÏû• Ïã§Ìå®")
                                            // TODO: Ïã§Ìå® Ï≤òÎ¶¨
                                        }
                                    )
                                } catch (e: Exception) {
                                    Log.e("ExitFlow", "‚ùå Ï†úÌïúÏãúÍ∞Ñ STT Ïã§Ìå®: ${e.message}")

                                    Toast.makeText(context, "ÏùåÏÑ± Ïù∏ÏãùÏóê Ïã§Ìå®ÌñàÏñ¥Ïöî.\nÎã§Ïãú ÏãúÎèÑÌï¥Ï£ºÏÑ∏Ïöî.", Toast.LENGTH_LONG).show()

                                    viewModel.deleteSpeech(
                                        onSuccess = {
                                            navController.navigate(
                                                route = NavRoutes.RandomSpeechLanding.route,
                                                navOptions = navOptions {
                                                    popUpTo(NavRoutes.RandomSpeechLanding.route) { inclusive = true }
                                                }
                                            )
                                        },
                                        onError = {
                                            Toast.makeText(context, "Ïó∞Ïäµ Îç∞Ïù¥ÌÑ∞ ÏÇ≠Ï†úÏóê Ïã§Ìå®ÌñàÏñ¥Ïöî.", Toast.LENGTH_SHORT).show()
                                        }
                                    )
                                }

                            }
                        }
                    }
                )
            }
            // 30Ï¥à ÎØ∏Îßå Ï¢ÖÎ£å Îã§Ïù¥ÏñºÎ°úÍ∑∏
            if (showExitConfirmDialog) {
                ExitConfirmDialog(
                    onDismiss = { showExitConfirmDialog = false },
                    onCancel = { showExitConfirmDialog = false },
                    onConfirm = {
                        showExitConfirmDialog = false
                        viewModel.deleteSpeech(
                            onSuccess = {
                                navController.navigate(
                                    route = NavRoutes.RandomSpeechLanding.route,
                                    navOptions = navOptions {
                                        popUpTo(NavRoutes.RandomSpeechLanding.route) { inclusive = true }
                                    }
                                )
                            },
                            onError = {
                                Log.d("RandomSpeech", "‚ùå ÏÇ≠Ï†ú Ïã§Ìå®: ${viewModel.uiState.value.errorMessage}")
                            }
                        )
                    }
                )
            }
        }
        // Ï§ëÏïô ÌÉÄÏù¥Î®∏
        if (prepCountdown > 0) {
            Box(modifier = Modifier.align(Alignment.Center)) {
                CommonTimer(
                    timeText = prepCountdown.toString(),
                    type = TimerType.CIRCLE
                )
            }
        }

        if (uiState.isLoading) {
            LoadingInProgressView(
                imageRes = R.drawable.character_home_5,
                message = "Î¶¨Ìè¨Ìä∏Î•º ÏÉùÏÑ±Ï§ëÏù¥ÏóêÏöî.\nÏû†ÏãúÎßå Í∏∞Îã§Î†§Ï£ºÏÑ∏Ïöî!"
            )
        }
    }
    if (uiState.isLoading) {
        LoadingInProgressView(
            imageRes = R.drawable.character_home_5,
            message = "Ïä§ÌÅ¨Î¶ΩÌä∏Î•º Ï†ÄÏû• Ï§ëÏù¥ÏóêÏöî.\nÏû†ÏãúÎßå Í∏∞Îã§Î†§Ï£ºÏÑ∏Ïöî!",
        )
    }
}